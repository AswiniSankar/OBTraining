package com.corejava.assignments;

public class EvaluationOfExperssion {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;
		int c = 1;
		int m;
		int n;
		int x = 10;
		int y = 2;
		int p = 2;
		m = ++a * 5;
		n = b++ - c * 2;
		p *= x / y;
		System.out.println(m + "\n" + n + "\n" + p);
	}

}
